def get_attractions(
    destination: str,
    categories: list[str] | None = None,
) -> list[dict]:
    """Get attractions for a destination, optionally filtered by categories."""
    results = [
        a for a in ATTRACTIONS if destination.lower() in a["destination"].lower()
    ]

    if not results:
        return results

    if categories:
        mapped_categories = {
            "自然": "自然", "nature": "自然",
            "文化": "文化", "culture": "文化",
            "美食": "美食", "food": "美食",
            "冒险": "冒险", "adventure": "冒险",
            "购物": "购物", "shopping": "购物",
        }
        cat_set = {mapped_categories.get(c.lower(), c) for c in categories}
        results = [a for a in results if a["category"] in cat_set]

    results.sort(key=lambda a: a["rating"], reverse=True)
    return results
