def get_weather(destination: str, month: str) -> dict | None:
    """Get weather data for a destination and month. Returns None if not found."""
    city_data = None
    for city, data in WEATHER.items():
        if destination.lower() in city.lower() or city.lower() in destination.lower():
            city_data = data
            city_name = city
            break

    if not city_data:
        return None

    # Fuzzy match month
    month_data = None
    for m, d in city_data.items():
        if month.lower() in m.lower() or m.lower() in month.lower():
            month_data = d
            actual_month = m
            break

    if not month_data:
        return None

    return {
        "destination": city_name,
        "month": actual_month,
        **month_data,
        "travel_advice": _get_season_advice(month_data["condition"]),
    }


def _get_season_advice(condition: str) -> str:
    if "雨" in condition or "台风" in condition:
        return "建议携带雨具，选择室内活动作为备选方案"
    if "热" in condition or "闷" in condition:
        return "注意防暑防晒，避免中午户外活动"
    if "冷" in condition:
        return "带上保暖衣物，早晚温差较大"
    return "气温舒适，非常适合户外活动和观光"
