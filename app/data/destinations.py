def search_destinations(
    query: str = "",
    interests: list[str] | None = None,
    budget_level: str | None = None,
) -> list[dict]:
    """Search destinations by keyword, interests, and budget level."""
    results = DESTINATIONS

    if query:
        query_lower = query.lower()
        results = [
            d
            for d in results
            if query_lower in d["name"].lower()
            or query_lower in d["country"].lower()
            or query_lower in d["region"].lower()
            or any(query_lower in tag for tag in d["tags"])
            or query_lower in d["type"]
        ]

    if interests:
        results = [
            d
            for d in results
            if any(
                interest.lower() in " ".join(d["tags"]).lower()
                for interest in interests
            )
        ]

    if budget_level:
        budget_map = {
            "经济": "经济",
            "budget": "经济",
            "中等": "中等",
            "mid-range": "中等",
            "mid range": "中等",
            "奢华": "奢华",
            "luxury": "奢华",
        }
        mapped = budget_map.get(budget_level.lower(), budget_level)
        results = [d for d in results if d["budget_level"] == mapped]

    return results
