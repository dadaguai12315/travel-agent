def get_hotels(
    destination: str,
    budget_level: str | None = None,
    min_rating: float | None = None,
) -> list[dict]:
    """Get hotel listings for a destination, optionally filtered by budget and rating."""
    results = [
        h for h in HOTELS if destination.lower() in h["destination"].lower()
    ]

    if not results:
        return results

    if budget_level:
        budget_map = {
            "经济": "经济", "budget": "经济",
            "中等": "中等", "mid-range": "中等", "mid range": "中等",
            "奢华": "奢华", "luxury": "奢华",
        }
        mapped = budget_map.get(budget_level.lower(), budget_level)
        results = [h for h in results if h["budget_level"] == mapped]

    if min_rating is not None:
        results = [h for h in results if h["rating"] >= min_rating]

    results.sort(key=lambda h: h["rating"], reverse=True)
    return results
